package com.bean;

public class Train {
    private String id;
    private int start;
    private int end;
    private String date;
    private int timenum;
    private double price;
    private int num;
    private int nowt;
    private String ago;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getTimenum() {
        return timenum;
    }

    public void setTimenum(int timenum) {
        this.timenum = timenum;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getNowt() {
        return nowt;
    }

    public void setNowt(int nowt) {
        this.nowt = nowt;
    }

    public String getAgo() {
        return ago;
    }

    public void setAgo(String ago) {
        this.ago = ago;
    }
}
